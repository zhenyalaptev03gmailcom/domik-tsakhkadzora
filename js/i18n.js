(function () {
  const storeKey = 'siteLang';

  function getLang() {
    return localStorage.getItem(storeKey) || 'ru';
  }

  function setLang(lang) {
    localStorage.setItem(storeKey, lang);
    document.documentElement.lang = lang;
    apply(lang);
    document.querySelectorAll('.lang-switch button').forEach((btn) => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  }

  function apply(lang) {
    const dict = (window.SITE_I18N && window.SITE_I18N[lang]) || {};
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      if (!dict[key]) return;
      if (el.classList.contains('i18n-pre')) {
        el.innerHTML = dict[key].replace(/\n/g, '<br>');
      } else {
        el.textContent = dict[key];
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key]) el.placeholder = dict[key];
    });
    const titleKey = document.querySelector('title[data-i18n]');
    if (titleKey) {
      const k = titleKey.getAttribute('data-i18n');
      if (dict[k]) document.title = dict[k];
    } else if (dict['page.title']) {
      document.title = dict['page.title'];
    }
    if (window.onLangChange) window.onLangChange(lang, dict);
  }

  document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.lang-switch button').forEach((btn) => {
      btn.addEventListener('click', () => setLang(btn.dataset.lang));
    });
    setLang(getLang());
  });
})();
